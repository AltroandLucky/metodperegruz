#include <iostream>
using namespace std;

class Point
{
    int x;
    int y;
public:
    //И так вот наш конструктор
    Point()
    {
        cout << "Construct\n";
        x = y = 0;
    }

    // Тоже конструктор но с 2 параметрами для инцилизации
    Point(int x1, int y1)
    {
        cout << "Construct by 2 param\n";
        x = x1;
        y = y1;
    }

    // Это метод для случайной инцилизации
    void Init()
    {
        x = rand() % 10;
        y = rand() % 10;
    }

    // Это метод для значений задания вручную
    void Init(int x1, int y1)
    {
        x = x1;
        y = y1;
    }

    // И этот метод показывает координаты
    void Output()
    {
        cout << "X: " << x << "\tY: " << y << endl;
    }

    //А это перегруз Оператара для наших Point
    Point operator+(Point& b)
    {
        Point temp;
        temp.x = this->x + b.x;
        temp.y = this->y + b.y;
        return temp;
    }

    // Вот перегруз оператора  для добавления числа к координатам
    Point operator+(int a)
    {
        Point temp(x + a, y + a);
        return temp;
    }

    // Ваши любимые Геттеры для координат
    int GetX() { return x; }
    int GetY() { return y; }

    // И Сеттеры для координат (честно меня так веселило когда вы говорили Гетереры сеторы=3 )
    void SetX(int a) { x = a; }
    void SetY(int a) { y = a; }
};

// Снова перегрузка оператора но для добавления числа к объекту Point (тоесть int + Point)
Point operator+(int a, Point b)
{
    Point rez;
    rez.SetX(b.GetX() + a);
    rez.SetY(b.GetY() + a);
    return rez;
}

// Угадай что это перегрузка оператора префиксного инкремента и теперь для (++a)
Point operator++(Point& a) // ++a запомни(префиксный инкремент для переменых)
{
    a.SetX(a.GetX() + 10);
    a.SetY(a.GetY() + 10);
    return a;
}

// Перегрузка оператора постфиксного инкремента (a++)
Point operator++(Point& a, int) // a++
{
    Point temp = a;  // мы тут сохраняем текущие значения во временный объект
    a.SetX(a.GetX() + 10);
    a.SetY(a.GetY() + 10);
    return temp;  // а тут возвращаем старые значения
}

int main()
{
    srand(unsigned(time(0))); // Тут инициализация генератора случайных чисел да он снова тут

    // И финал близок теперь мы создаем объекты с начальными координатами
    Point a(1, 2);
    Point b(3, 4);

    // Причем пример использования оператора сложения для двух объектов
    Point c = a + b; // Создается новым объектом c с координатами (4, 6)
    c.Output();  // И выводим результат: X: 4 Y: 6

    // Туточки пример использования оператора сложения с числом
    Point d = a + 10; // Добавляем 10 к обеим координатам a
    d.Output();  // и выводим результат: X: 11 Y: 12

    // Пример использования оператора сложения (int + Point)
    Point e = 10 + a; // Число 10 добавляется к объекту a
    e.Output();  // Выводим результат: X: 11 Y: 12

    // Пример использования инкремента (префиксного и постфиксного)
    Point f = ++a;  // Префиксный инкремент
    f.Output();  // Выводим результат: X: 11 Y: 12
    a.Output();  // Выводим результат: X: 11 Y: 12

    Point g = a++;  // Постфиксный инкремент
    g.Output();  // Выводим старые значения: X: 11 Y: 12
    a.Output();  // Выводим новые значения: X: 21 Y: 22

    return 0;
}
